# Dette programmet gir en personlig hilsen til bruker, ved å bruke input som brukeren selv har lagt inn.

navn = input ("Hei Student! Hva heter du?: ")
# Her har jeg laget en variabel kalt "navn", som inneholder en tekststring og funksjonen "input".
# Bruker får spørsmål om hva den heter, og mulighet til å skrive sitt eget navn etter kolon.

print ("Hei,", navn, ":-)")
